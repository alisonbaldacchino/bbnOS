typedef unsigned short u16;
typedef unsigned char u8;
typedef unsigned long ulong;
typedef long unsigned int size_t;
typedef long int ptrdiff_t;
typedef unsigned int wint_t;

typedef enum { false = 0, true = 1 } bool;

